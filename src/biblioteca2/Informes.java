    public static String FechaActual(){
        Date fecha= new Date();
        SimpleDateFormat formatoFecha=new SimpleDateFormat("yyyy-MM-dd");
        return formatoFecha.format(fecha);
    }
    
    public static String FechaFinal() {
        // Obtener la fecha actual
        Date fecha = new Date();

        // Crear un objeto Calendar
        Calendar calendario = Calendar.getInstance();
        calendario.setTime(fecha);

        // Sumar 5 días
        calendario.add(Calendar.DAY_OF_MONTH, 5);

        // Obtener la fecha después de sumar 5 días
        Date fechaMas5Dias = calendario.getTime();

        // Formatear la fecha en el formato deseado
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        return formatoFecha.format(fechaMas5Dias);
    }

 

