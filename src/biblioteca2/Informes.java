    private void AsignarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AsignarLibroActionPerformed
        // TODO add your handling code here:
        Biblioteca2  biblioteca2 = new Biblioteca2();
        Object seleccionLibro = ComboLibro.getSelectedItem();
        Object seleccionCliente = ComboCliente.getSelectedItem();
    
    if (seleccionLibro == null || seleccionCliente == null) {
        JOptionPane.showMessageDialog(this, "Por favor, selecciona un Libro y un Cliente para el Préstamo.");
        return;
    }
    String nombreLibro = seleccionLibro.toString();
    String nombreUsuario = seleccionCliente.toString();
    String idLibro = biblioteca2.obtenerIdLibro(nombreLibro);
    String idUsuario = biblioteca2.obtenerIdUsuario(nombreUsuario);
    
    if (idLibro != null && idUsuario != null) {
        String fechaPrestamo = FechaActual();  //Implementa esta función para obtener la fecha actual
        String fechaDevolucion = FechaFinal(); // Implementa esta función para obtener la fecha actual
        
        boolean asignacionExitosa = biblioteca2.AsignarLibro(idLibro, idUsuario, fechaPrestamo, fechaDevolucion);
        
        if (asignacionExitosa) {
            JOptionPane.showMessageDialog(this, "El libro ha sido asignado al cliente correctamente.");
            JOptionPane.showMessageDialog(this, "Recuerda que tienes 5 dias para devolver el libro.");

        } else {
            JOptionPane.showMessageDialog(this, "Error al asignar el libro al cliente. Por favor, inténtalo de nuevo.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "No se pudo obtener el ID del libro o del usuario. Verifica la selección.");
    }
    }//GEN-LAST:event_AsignarLibroActionPerformed

